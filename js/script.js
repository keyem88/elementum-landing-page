document.addEventListener('DOMContentLoaded', () => {
    console.log("Quatralor: DOM Content Loaded. Initializing...");

    // --- Supabase Config ---
    const SUBAPASE_URL = "https://tyubexicjvaeuvhlnyfd.supabase.co";
    const SUPABASE_KEY = "sb_publishable_zXY_2MpFR0gdd9fxPMLlRg_x8OD0MOb";
    let supabase = null;
    
    try {
        if (window.supabase) {
            supabase = window.supabase.createClient(SUBAPASE_URL, SUPABASE_KEY);
            console.log("Quatralor: Supabase initialized.");
        } else {
            console.warn("Quatralor: Supabase SDK not found.");
        }
    } catch (e) {
        console.error("Quatralor: Failed to initialize Supabase:", e);
    }

    // --- i18n Logic ---
    const langBtns = document.querySelectorAll('.lang-btn');
    let currentLang = localStorage.getItem('preferredLang') || 'en';

    function setLanguage(lang) {
        console.log("Quatralor: Setting language to", lang);
        if (!translations[lang]) {
            console.error("Quatralor: Translation not found for", lang);
            return;
        }
        
        currentLang = lang;
        localStorage.setItem('preferredLang', lang);

        langBtns.forEach(btn => {
            btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
        });

        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang][key]) {
                el.innerHTML = translations[lang][key];
            }
        });

        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (translations[lang][key]) {
                el.placeholder = translations[lang][key];
            }
        });

        const metaDesc = document.querySelector('meta[data-i18n-meta="description"]');
        if (metaDesc) {
            metaDesc.setAttribute('content', translations[lang]['description']);
        }
        
        document.title = translations[lang]['title'];
    }

    langBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            setLanguage(btn.getAttribute('data-lang'));
        });
    });

    setLanguage(currentLang);

    // Parallax effect for the hero card
    const heroCard = document.querySelector('.hero-visual');
    const heroSection = document.querySelector('.hero');

    if (heroSection && heroCard) {
        heroSection.addEventListener('mousemove', (e) => {
            const { clientX, clientY } = e;
            const { innerWidth, innerHeight } = window;
            const xPos = (clientX / innerWidth - 0.5) * 40;
            const yPos = (clientY / innerHeight - 0.5) * 40;
            heroCard.style.transform = `translate(${xPos}px, ${yPos}px) rotateX(${-yPos/2}deg) rotateY(${xPos/2}deg)`;
        });

        heroSection.addEventListener('mouseleave', () => {
            heroCard.style.transform = `translate(0, 0) rotateX(0) rotateY(0)`;
        });
    }

    // Intersection Observer for fade-in animations
    const faders = document.querySelectorAll('.fade-in');
    if (faders.length > 0) {
        const appearOptions = {
            threshold: 0.1,
            rootMargin: "0px 0px -50px 0px"
        };

        const appearOnScroll = new IntersectionObserver((entries, observer) => {
            entries.forEach(entry => {
                if (!entry.isIntersecting) return;
                entry.target.classList.add('appear');
                observer.unobserve(entry.target);
            });
        }, appearOptions);

        faders.forEach(fader => appearOnScroll.observe(fader));
    }

    // Beta Form Submission
    const betaForm = document.getElementById('beta-form');
    if (betaForm) {
        betaForm.addEventListener('submit', async (e) => {
            e.preventDefault();
            console.log("Quatralor: Submit button clicked.");
            const email = betaForm.querySelector('input[type="email"]').value;
            const element = betaForm.querySelector('select').value;
            const submitBtn = betaForm.querySelector('button[type="submit"]');
            
            if (submitBtn) submitBtn.disabled = true;

            try {
                if (supabase) {
                    const { error } = await supabase
                        .from('beta_signups')
                        .insert([
                            { email, favorite_element: element, language: currentLang }
                        ]);

                    if (error) throw error;
                }

                // Show success message with sharing options
                const shareText = currentLang === 'de' 
                    ? "Kein Duellant kämpft allein. Quatralor findet am selben Tisch statt. Rekrutiere jetzt deine Verbündeten für die Vorhut!" 
                    : "No duelist fights alone. Quatralor happens across the table. Recruit your allies for the Vanguard now!";
                const shareUrl = "https://keyem88.github.io/elementum-landing-page/";
                
                betaForm.innerHTML = `
                    <div class="success-message fade-in" style="padding: 2rem; border-radius: 12px; background: rgba(50, 215, 75, 0.1); border: 1px solid var(--accent-earth);">
                        <h3 style="color: var(--accent-earth); margin-bottom: 1rem;">${translations[currentLang].beta_success}</h3>
                        <p style="color: var(--text-muted); margin-bottom: 2rem;">${translations[currentLang].beta_success_desc} <br><strong>${email}</strong>.</p>
                        
                        <div class="share-section">
                            <h4 style="font-size: 0.9rem; margin-bottom: 1rem; color: var(--text-main);">${translations[currentLang].beta_share_title}</h4>
                            <p style="font-size: 0.8rem; color: var(--text-muted); margin-bottom: 1.5rem;">${translations[currentLang].beta_share_subtitle}</p>
                            <div class="share-buttons">
                                <a href="https://twitter.com/intent/tweet?text=${encodeURIComponent(shareText)}&url=${encodeURIComponent(shareUrl)}" target="_blank" class="share-btn x" title="X (Twitter)">
                                    <i class="fa-brands fa-x-twitter"></i>
                                </a>
                                <a href="https://api.whatsapp.com/send?text=${encodeURIComponent(shareText + " " + shareUrl)}" target="_blank" class="share-btn whatsapp" title="WhatsApp">
                                    <i class="fa-brands fa-whatsapp"></i>
                                </a>
                                <a href="https://www.instagram.com/quatralor/" target="_blank" class="share-btn instagram" title="Instagram">
                                    <i class="fa-brands fa-instagram"></i>
                                </a>
                            </div>
                        </div>
                    </div>
                `;
            } catch (err) {
                console.error("Quatralor: Signup error:", err);
                if (submitBtn) submitBtn.disabled = false;
                alert("Something went wrong. Please try again.");
            }
        });
    }

    // Video Modal Logic
    const videoModal = document.getElementById('videoModal');
    const demoVideo = document.getElementById('demoVideo');
    const watchDemoBtn = document.querySelector('a[href="#demo"]');
    const closeModal = document.querySelector('.close-modal');

    if (watchDemoBtn && videoModal && demoVideo) {
        console.log("Quatralor: Video modal initialized.");
        watchDemoBtn.addEventListener('click', (e) => {
            e.preventDefault();
            console.log("Quatralor: Playing demo video.");
            videoModal.style.display = 'flex';
            setTimeout(() => videoModal.classList.add('show'), 10);
            demoVideo.play().catch(err => console.warn("Quatralor: Auto-play failed:", err));
        });

        const closeHandler = () => {
            videoModal.classList.remove('show');
            setTimeout(() => {
                videoModal.style.display = 'none';
                demoVideo.pause();
                demoVideo.currentTime = 0;
            }, 300);
        };

        if (closeModal) {
            closeModal.addEventListener('click', closeHandler);
        }
        
        window.addEventListener('click', (e) => {
            if (e.target === videoModal) {
                closeHandler();
            }
        });

        // Close on Escape key
        window.addEventListener('keydown', (e) => {
            if (e.key === 'Escape' && videoModal.classList.contains('show')) {
                closeHandler();
            }
        });
    } else {
        console.warn("Quatralor: Video modal elements not found.");
    }

    console.log("Quatralor: Initialization complete.");
});
